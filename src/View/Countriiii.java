    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterBtnActionPerformed
          if(nameTXT.getText().trim().isEmpty()|cityTXT.getText().trim().isEmpty())  {
       JOptionPane.showMessageDialog(null, "All fields are Mandatory","Mandatory Field",JOptionPane.WARNING_MESSAGE);  
    }else{
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 5000);
            CountryService paises =(CountryService) reg.lookup("pais");
             Country cnt=new Country();
                cnt.setName(nameTXT.getText());
                cnt.setCity(cityTXT.getText());
                Country thecnt=paises.RegisterCountries(cnt);
         
            
            if(thecnt!=null){
                JOptionPane.showMessageDialog(this, "country Saved");
                displaydata();
            }else{
                 JOptionPane.showMessageDialog(this, "Country couldn't be saved");
            }
           
        } catch (Exception e) {
            e.printStackTrace();
        }
          }
    }//GEN-LAST:event_RegisterBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        try{
        Registry reg=LocateRegistry.getRegistry("127.0.0.1", 5000);
            CountryService paises =(CountryService) reg.lookup("pais");
            Country cnt=new Country();
           cnt.setCountryId( Integer.parseInt(idTXT.getText()));
            Country thecnt=paises.SearchCountries(cnt);
           if(thecnt!=null){
               nameTXT.setText(thecnt.getName());
               cityTXT.setText(thecnt.getCity());
              
           }else{
               JOptionPane.showMessageDialog(this,"Country ID not Exist! Register Now");
        }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
      if(nameTXT.getText().trim().isEmpty()|cityTXT.getText().trim().isEmpty())  {
       JOptionPane.showMessageDialog(null, "All fields are Mandatory","Mandatory Field",JOptionPane.WARNING_MESSAGE);  
    }else{
      
      try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 5000);
             CountryService paises =(CountryService) reg.lookup("pais");
            Country cnt=new Country();
           cnt.setCountryId( Integer.parseInt(idTXT.getText()));
           cnt.setName(nameTXT.getText());
           cnt.setCity(cityTXT.getText());
           Country thecnt=paises.UpdateCountries(cnt);
            if(thecnt!=null){
                JOptionPane.showMessageDialog(this, "Data Updated Successful");
                idTXT.setText("");
              nameTXT.setText("");
              cityTXT.setText("");
                 displaydata();
            }else{
                 JOptionPane.showMessageDialog(this, "Data not Updated");
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
      }
    }//GEN-LAST:event_UpdateBtnActionPerformed

