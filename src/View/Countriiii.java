    private void displaydata(){
        
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 5000);
              CountryService paises =(CountryService) reg.lookup("pais");
           List<Country> thecnt=paises.AllCountries();
            DefaultTableModel tmodel=(DefaultTableModel)countryTb.getModel();
            tmodel.setRowCount(0);
           if(thecnt!=null){
               for(Country cnt:thecnt){
                   Object data[]={
                       
                      cnt.getCountryId(),
                   cnt.getName(),
                   cnt.getCity()
                   };
                   tmodel.addRow(data);
                  
               }
           }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
